        print(f"üìà Training samples: {len(train_df):,}")
        print(f"üìâ Test samples: {len(test_df):,}")
        print(f"üìä Total samples: {len(train_df) + len(test_df):,}")

        # Overall composition
        prototypes = train_df['is_prototype'].sum()
