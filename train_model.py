            for source in train_df['source'].unique():
                if 'prototype' not in source.lower():
                    source_df = train_df[train_df['source'] == source]

                    original = ((source_df['source'] == source) &
                              (~source_df['is_paraphrased']) &
                              (~source_df['is_prototype'])).sum()
